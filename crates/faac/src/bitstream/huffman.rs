// --- Constants (huff2.h) ------------------------------------------------------

pub const HCB_ZERO: i32 = 0;
pub const HCB_ESC: i32 = 11;
pub const HCB_PNS: i32 = 13;
pub const HCB_INTENSITY2: i32 = 14;
pub const HCB_INTENSITY: i32 = 15;
pub const HCB_NONE: i32 = 16;

pub const MAX_HUFF_ESC_VAL: i32 = 8191;
pub const SF_OFFSET: i32 = 100;
pub const SF_MIN: i32 = 10;
pub const SF_PNS_OFFSET: i32 = SF_OFFSET - SF_MIN;

#[inline]
pub fn clamp_sf_diff(diff: i32) -> i32 {
    diff.clamp(-SF_DELTA, SF_DELTA)
}

// --- Internal helpers ---------------------------------------------------------

/// Encode an AAC ESC value (used for spectral coefficients ≥ 16). Returns the
/// total bit length of the produced codeword (stored in `*code`).
fn escape(x: i32, code: &mut i32) -> i32 {
    if x > MAX_HUFF_ESC_VAL {
        eprintln!("huff2::escape: x_quant > {}", MAX_HUFF_ESC_VAL);
        return 0;
    }

    let mut preflen: i32 = 0;
    let mut base: i32 = 32;
    *code = 0;

    while base <= x {
        base <<= 1;
        *code <<= 1;
        *code |= 1;
        preflen += 1;
    }
    base >>= 1;

    // Separator bit (0).
    *code <<= 1;

    // Mantissa.
    *code <<= preflen + 4;
    *code |= x - base;

    (preflen << 1) + 5
}

/// Return the book table for a given book index (1..=11). Book 0 (HCB_ZERO)
/// has no entries; callers never request it here.
fn book_for(bnum: i32) -> &'static [Hcode16] {
    match bnum {
        1 => &BOOK01,
        2 => &BOOK02,
        3 => &BOOK03,
        4 => &BOOK04,
        5 => &BOOK05,
        6 => &BOOK06,
        7 => &BOOK07,
        8 => &BOOK08,
        9 => &BOOK09,
        10 => &BOOK10,
        HCB_ESC => &BOOK11, // 11
        _ => unreachable!("book_for called with invalid bnum {}", bnum),
    }
}

/// Push a finished spectral codeword into `coder->s[]` and bump `datacnt`.
#[inline]
fn store_spectral(coder: &mut CoderInfo, data: i32, len: i32) {
    let datacnt = coder.datacnt as usize;
    coder.s[datacnt] = SpectralData { data, len };
    coder.datacnt += 1;
}

/// Append sign bits for non-zero values, returning (data_with_signs, extra_bits).
#[inline]
fn append_sign_bits(qp: &[i32], mut data: i32) -> (i32, i32) {
    let mut extra = 0i32;
    for &q in qp {
        if q != 0 {
            extra += 1;
            data <<= 1;
            if q < 0 {
                data |= 1;
            }
        }
    }
    (data, extra)
}

/// Count sign bits for non-zero values.
#[inline]
fn count_sign_bits(qp: &[i32]) -> i32 {
    qp.iter().filter(|&&q| q != 0).count() as i32
}

/// Encode `qs[0..len]` with book `bnum`.
///
/// When `coder` is `Some`, the produced codewords are appended to
/// `coder.s[]` (so the syntax layer can emit them later via `put_bit`).
/// When `None`, only the bit count is computed. Returns the total bit count
/// for the chunk, or -1 on bounds-check failure.
fn huffcode(qs: &[i32], len: usize, bnum: i32, mut coder: Option<&mut CoderInfo>) -> i32 {
    let book = book_for(bnum);
    let mut bits = 0i32;

    match bnum {
        1 | 2 => {
            for qp in qs[..len].chunks_exact(4) {
                let idx = 27 * qp[0] + 9 * qp[1] + 3 * qp[2] + qp[3] + 40;
                if idx < 0 || idx as usize >= book.len() {
                    return -1;
                }
                let blen = book[idx as usize].len as i32;
                if let Some(c) = coder.as_deref_mut() {
                    store_spectral(c, book[idx as usize].data as i32, blen);
                }
                bits += blen;
            }
        }
        3 | 4 => {
            for qp in qs[..len].chunks_exact(4) {
                let idx = 27 * qp[0].abs() + 9 * qp[1].abs() + 3 * qp[2].abs() + qp[3].abs();
                if idx < 0 || idx as usize >= book.len() {
                    return -1;
                }
                let mut blen = book[idx as usize].len as i32;
                if let Some(c) = coder.as_deref_mut() {
                    let (data, extra) = append_sign_bits(qp, book[idx as usize].data as i32);
                    blen += extra;
                    store_spectral(c, data, blen);
                } else {
                    blen += count_sign_bits(qp);
                }
                bits += blen;
            }
        }
        5 | 6 => {
            for qp in qs[..len].chunks_exact(2) {
                let idx = 9 * qp[0] + qp[1] + 40;
                if idx < 0 || idx as usize >= book.len() {
                    return -1;
                }
                let blen = book[idx as usize].len as i32;
                if let Some(c) = coder.as_deref_mut() {
                    store_spectral(c, book[idx as usize].data as i32, blen);
                }
                bits += blen;
            }
        }
        7 | 8 => {
            for qp in qs[..len].chunks_exact(2) {
                let idx = 8 * qp[0].abs() + qp[1].abs();
                if idx < 0 || idx as usize >= book.len() {
                    return -1;
                }
                let mut blen = book[idx as usize].len as i32;
                if let Some(c) = coder.as_deref_mut() {
                    let (data, extra) = append_sign_bits(qp, book[idx as usize].data as i32);
                    blen += extra;
                    store_spectral(c, data, blen);
                } else {
                    blen += count_sign_bits(qp);
                }
                bits += blen;
            }
        }
        9 | 10 => {
            for qp in qs[..len].chunks_exact(2) {
                let idx = 13 * qp[0].abs() + qp[1].abs();
                if idx < 0 || idx as usize >= book.len() {
                    return -1;
                }
                let mut blen = book[idx as usize].len as i32;
                if let Some(c) = coder.as_deref_mut() {
                    let (data, extra) = append_sign_bits(qp, book[idx as usize].data as i32);
                    blen += extra;
                    store_spectral(c, data, blen);
                } else {
                    blen += count_sign_bits(qp);
                }
                bits += blen;
            }
        }
        HCB_ESC => {
            for qp in qs[..len].chunks_exact(2) {
                let x0 = qp[0].abs().min(16);
                let x1 = qp[1].abs().min(16);
                let idx = 17 * x0 + x1;
                if idx < 0 || idx as usize >= book.len() {
                    return -1;
                }

                let mut blen = book[idx as usize].len as i32;
                if let Some(c) = coder.as_deref_mut() {
                    let (data, extra) = append_sign_bits(qp, book[idx as usize].data as i32);
                    blen += extra;
                    store_spectral(c, data, blen);
                } else {
                    blen += count_sign_bits(qp);
                }
                bits += blen;

                if x0 >= 16 {
                    let mut esc_code = 0i32;
                    let esc_len = escape(qp[0].abs(), &mut esc_code);
                    if let Some(c) = coder.as_deref_mut() {
                        store_spectral(c, esc_code, esc_len);
                    }
                    bits += esc_len;
                }
                if x1 >= 16 {
                    let mut esc_code = 0i32;
                    let esc_len = escape(qp[1].abs(), &mut esc_code);
                    if let Some(c) = coder.as_deref_mut() {
                        store_spectral(c, esc_code, esc_len);
                    }
                    bits += esc_len;
                }
            }
        }
        _ => {
            eprintln!("huff2: book {} out of range", bnum);
            return -1;
        }
    }

    bits
}

// --- Methods on CoderInfo -----------------------------------------------------

impl CoderInfo {
    /// Choose the smallest book that encodes `qs[0..len]`, store it in
    /// `self.book[self.bandcnt]`, and (if non-zero) append the codewords to
    /// `self.s[]` for later bitstream emission.
    pub fn huffbook(&mut self, qs: &[i32], len: usize) -> i32 {
        let maxq = qs[..len].iter().map(|q| q.abs()).max().unwrap_or(0);

        let select_pair = |qs: &[i32], len: usize, base: i32| -> i32 {
            let l0 = huffcode(qs, len, base, None);
            let l1 = huffcode(qs, len, base + 1, None);
            if l1 < l0 { base + 1 } else { base }
        };

        let bookmin = if maxq < 1 {
            HCB_ZERO
        } else if maxq < 2 {
            select_pair(qs, len, 1)
        } else if maxq < 3 {
            select_pair(qs, len, 3)
        } else if maxq < 5 {
            select_pair(qs, len, 5)
        } else if maxq < 8 {
            select_pair(qs, len, 7)
        } else if maxq < 13 {
            select_pair(qs, len, 9)
        } else {
            HCB_ESC
        };

        if bookmin > HCB_ZERO {
            let _ = huffcode(qs, len, bookmin, Some(self));
        }
        let bandcnt = self.bandcnt as usize;
        self.book[bandcnt] = bookmin;

        0
    }

    /// Emit the section_data() syntax element (book IDs + run lengths) and
    /// return its bit count. With `write=false` only counts.
    pub fn write_books(&self, stream: &mut BitStream, write: bool) -> i32 {
        let (maxcnt, cntbits) = if self.block_type == WindowType::OnlyShortWindow {
            (7, 3)
        } else {
            (31, 5)
        };
        let bookbits = 4;
        let mut bits = 0i32;
        let sfbn = self.sfbn as i32;

        for group in 0..self.groups.n {
            let mut band = group * sfbn;
            let maxband = band + sfbn;

            while band < maxband {
                let book = self.book[band as usize];
                band += 1;
                let mut bookcnt = 1i32;
                if write {
                    stream.put_bit(book as u64, bookbits);
                }
                bits += bookbits;

                if band < maxband {
                    while book == self.book[band as usize] {
                        band += 1;
                        bookcnt += 1;
                        if band >= maxband {
                            break;
                        }
                    }
                }

                while bookcnt >= maxcnt {
                    if write {
                        stream.put_bit(maxcnt as u64, cntbits);
                    }
                    bits += cntbits;
                    bookcnt -= maxcnt;
                }
                if write {
                    stream.put_bit(bookcnt as u64, cntbits);
                }
                bits += cntbits;
            }
        }

        bits
    }

    /// Emit the scalefactor section (delta-coded, with separate trackers for
    /// regular SF, intensity stereo, and PNS).
    pub fn write_sf(&self, stream: &mut BitStream, write: bool) -> i32 {
        let mut bits = 0i32;
        let mut lastsf = self.global_gain;
        let mut lastis = 0i32;
        let mut lastpns = self.global_gain - SF_PNS_OFFSET;
        let mut initpns = true;

        for cnt in 0..self.bandcnt as usize {
            let book = self.book[cnt];

            if book == HCB_INTENSITY || book == HCB_INTENSITY2 {
                let mut diff = self.sf[cnt] - lastis;
                diff = clamp_sf_diff(diff);
                let entry = BOOK12[(SF_DELTA + diff) as usize];
                let length = entry.len as i32;
                bits += length;
                lastis += diff;
                if write {
                    stream.put_bit(entry.data as u64, length);
                }
            } else if book == HCB_PNS {
                let mut diff = self.sf[cnt] - lastpns;
                if initpns {
                    initpns = false;
                    let length = 9;
                    bits += length;
                    lastpns += diff;
                    if write {
                        stream.put_bit((diff + 256) as u64, length);
                    }
                    continue;
                }
                diff = clamp_sf_diff(diff);
                let entry = BOOK12[(SF_DELTA + diff) as usize];
                let length = entry.len as i32;
                bits += length;
                lastpns += diff;
                if write {
                    stream.put_bit(entry.data as u64, length);
                }
            } else if book != HCB_ZERO && book != HCB_NONE {
                let mut diff = self.sf[cnt] - lastsf;
                diff = clamp_sf_diff(diff);
                let entry = BOOK12[(SF_DELTA + diff) as usize];
                let length = entry.len as i32;
                bits += length;
                lastsf += diff;
                if write {
                    stream.put_bit(entry.data as u64, length);
                }
            }
        }

        bits
    }
}

